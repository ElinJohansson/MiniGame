public class Color {
    private int red;
    private int blue;
    private int yellow;

    public Color(int red, int blue, int yellow ){
        this.red = red;
        this.blue = blue;
        this.yellow = yellow;
    }

    public int getRed() {
        return red;
    }

    public int getBlue() {
        return blue;
    }

    public int getYellow() {
        return yellow;
    }
}
